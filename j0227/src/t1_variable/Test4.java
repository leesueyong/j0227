package t1_variable;
//실수자료형(float(4byte), double(8byte))
public class Test4 {
	public static void main(String[] args) {
		double dd1,dd2;
		dd1 = 3.141592;
		dd2 = 3.1423123123;
		System.out.println(dd1+"\n"+dd2);
		
		float ff1, ff2;
		ff1 = 3.14F;
		System.out.println(ff1);
		ff2 = 3.141231231231232323F;
		System.out.println(ff2);
   }
}
